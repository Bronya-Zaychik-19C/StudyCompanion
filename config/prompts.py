def get_feynman_opening(concept: str) -> str:
    """获取费曼学习法开场白"""
    import random
    options = [
        f"好的，我们来理解一下「{concept}」。先问问你，你觉得这是什么？",
        f"提到「{concept}」，你首先想到的是什么？",
        f"试着用自己的话说下，什么是「{concept}」？"
    ]
    return random.choice(options)


def get_feynman_followup(stage: str, user_input: str) -> str:
    """获取费曼学习法后续引导"""
    import random

    if stage == "identify":
        options = [
            "很好！能再具体说说吗？比如它在什么情况下会用到？",
            "嗯，那你能举个例子吗？",
            "我理解了。那如果要给一个完全不懂的人解释，你会怎么说？"
        ]
    elif stage == "simplify":
        options = [
            "这样说能理解吗？",
            "我换个说法，你看对不对...",
            "这样解释清楚了吗？还有什么疑问吗？"
        ]
    else:
        options = [
            "我们来总结一下，今天主要理解了什么？",
            "你觉得哪个部分最难理解？",
            "还有什么疑问吗？"
        ]

    return random.choice(options)
